def pitch_stats(path, target_sr=16000):
    y, sr = sf.read(path, dtype="float32")
    if y.ndim == 2:
        y = y.mean(axis=1)
    if sr != target_sr:
        y = librosa.resample(y, orig_sr=sr, target_sr=target_sr)
        sr = target_sr

    # pYIN returns f0 (Hz) with nan for unvoiced, and a voiced flag
    f0, voiced_flag, voiced_prob = librosa.pyin(
        y,
        fmin=librosa.note_to_hz("C2"),
        fmax=librosa.note_to_hz("C7"),
        sr=sr,
        frame_length=2048,
        hop_length=256
    )

    if f0 is None:
        return np.nan, np.nan, np.nan, 0.0

    voiced_f0 = f0[voiced_flag]
    voiced_ratio = float(np.mean(voiced_flag)) if len(voiced_flag) > 0 else 0.0

    if voiced_f0.size < 5:
        return np.nan, np.nan, np.nan, voiced_ratio

    # log-scale is better for pitch (cents-ish)
    log_f0 = np.log(voiced_f0)

    f0_mean = float(np.mean(log_f0))
    f0_std  = float(np.std(log_f0))
    # frame-to-frame instability
    f0_diff_std = float(np.std(np.diff(log_f0))) if log_f0.size >= 2 else np.nan

    return f0_mean, f0_std, f0_diff_std, voiced_ratio

