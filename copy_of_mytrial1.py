def evaluate(model, loader, device, return_preds=False):
    model.eval()
    ys, y_hats = [], []
    with torch.no_grad():
        for wav, mask, y in loader:
            wav, mask = wav.to(device), mask.to(device)
            z_hat = model(wav, mask)
