def make_bin_weights(mos_list, bins=(1,2,3,4,5.01), max_ratio=10.0):
    """
    bins: score buckets (4 bins by default)
    max_ratio: caps oversampling so rare bins don't dominate too hard
    """
    mos = np.array(mos_list, dtype=np.float32)
    bin_idx = np.digitize(mos, bins) - 1  # 0..len(bins)-2
    n_bins = len(bins) - 1

    counts = np.bincount(bin_idx, minlength=n_bins).astype(np.float32)
    counts[counts == 0] = 1.0  # avoid divide-by-zero if a bin is empty

    inv = 1.0 / counts
    inv = inv / inv.mean()  # normalize so average weight ~ 1

    # cap extreme oversampling
    inv = np.clip(inv, 1.0 / max_ratio, max_ratio)

    weights = inv[bin_idx]
    return torch.tensor(weights, dtype=torch.double), counts

