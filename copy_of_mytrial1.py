def pearson_corr(x, y, eps=1e-8):
    x = x - x.mean()
    y = y - y.mean()
    return (x * y).sum() / (
        torch.sqrt((x * x).sum() + eps) *
        torch.sqrt((y * y).sum() + eps)
    )

def hybrid_loss(z_hat, y_true_mos, MOS_MEAN, MOS_STD, alpha=0.6):
    # convert MOS to standardized z-space
    z_true = (y_true_mos - MOS_MEAN) / MOS_STD

    l1 = torch.nn.functional.l1_loss(z_hat, z_true)
    corr = pearson_corr(z_hat, z_true)

    # minimize L1 and maximize correlation
    return alpha * l1 + (1 - alpha) * (1 - corr)

def train(model, train_loader, val_loader, device, epochs=20, lr=1e-4):

    model.to(device)

    optimizer = torch.optim.Adam(
        list(model.regressor.parameters()) + list(model.norm.parameters()),
        lr=lr
    )

    best_p = -1

    for epoch in range(epochs):
        model.train()
        losses = []

        for wav, mask, y in tqdm(train_loader, desc=f"Epoch {epoch}"):

            wav = wav.to(device)
            mask = mask.to(device)
            y = y.to(device)

            z_hat = model(wav, mask)

            loss = hybrid_loss(
                z_hat,
                y,
                MOS_MEAN,
                MOS_STD,
                alpha=0.6
            )

            optimizer.zero_grad()
            loss.backward()
            torch.nn.utils.clip_grad_norm_(model.parameters(), 1.0)
            optimizer.step()

            losses.append(loss.item())

        # evaluate
        val_mae, val_rmse, val_p, val_s, ys, y_hats = evaluate(
            model, val_loader, device, return_preds=True
        )

        print(
            f"Epoch {epoch} | "
            f"Loss {np.mean(losses):.4f} | "
            f"Val MAE {val_mae:.3f} | "
            f"RMSE {val_rmse:.3f} | "
            f"P {val_p:.3f} | "
            f"S {val_s:.3f} | "
            f"y_std {ys.std():.3f} | "
            f"pred_std {y_hats.std():.3f}"
        )

        if val_p > best_p:
            best_p = val_p
            torch.save(model.state_dict(), f"{CKPT_DIR}/best_head.pt")

